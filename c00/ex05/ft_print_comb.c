#include <unistd.h>

void	ft_print_comb(void)
{
	int		i;
	int		j;
	int		k;
	char	out[3];

	i = -1;
	while (i++ < 9)
	{
		j = i;
		out[0] = i + 48;
		while (j++ < 9)
		{
			k = j;
			out[1] = j + 48;
			while (k++ < 9)
			{
				out[2] = k + 48;
				write(1, out, 3);
				if (i != 7 || j != 8 || k != 9)
				{
					write(1, ", ", 2);
				}
			}
		}
	}
}
