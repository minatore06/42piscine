#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	out[5];

	out[2] = ' ';
	i = 0;
	while (i <= 99)
	{
		j = i + 1;
		while (j <= 99)
		{
			out[0] = 48 + i / 10;
			out[1] = 48 + i % 10;
			out[3] = 48 + j / 10;
			out[4] = 48 + j % 10;
			write(1, out, 5);
			if (i != 98 || j != 99)
			{
				write(1, ", ", 2);
			}
			j++;
		}
		i++;
	}
}
