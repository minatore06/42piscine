unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int		i;

	if (size)
	{
		i = 0;
		while ((unsigned int)i < size - 1 && src[i])
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	i = 0;
	while (src[i] != '\0')
		i++;
	return (i);
}
